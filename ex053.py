# Faça um programa em Python que calcule 2 elevado à 30 e exiba o resultado na tela.

# Importa a função 'pow' do módulo 'math', que permite calcular potências.
from math import pow

# Calcula 2 elevado à potência de 30. A função pow retorna o valor de 2^30.
exponenciacao = pow(2,30)

# Exibe o resultado da exponenciação calculada.
print(exponenciacao)
